#include<math.h>

int square(int num){
    return (num * num);
}

int squareRoot(int num){
    return power(num , 1/2);
}

int cube(int num){
    return (num * num * num);
}

int cubeRoot(int num){
    return pow(num , 1/3);
}

int multiplication(int num1, int num2)
{
    return (num1*num2);
}

int division(int num1, int num2)
{
    return (num1/num2);
}

int remainder(int num1, int num2)
{
    return (num1%num2);
}
